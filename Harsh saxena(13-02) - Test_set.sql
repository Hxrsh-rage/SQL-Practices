SELECT W1.FIRST_NAME, W1.LAST_NAME, W1.SALARY 
FROM Worker W1 
WHERE EXISTS (
    SELECT 1 FROM Worker W2 
    WHERE W1.SALARY = W2.SALARY AND W1.WORKER_ID <> W2.WORKER_ID
)
ORDER BY W1.SALARY;

